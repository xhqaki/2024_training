destination=["paris","shanghai","dali","hangzhou","guangzhou"]
print(destination) #原来的顺序
print(sorted(destination)) #字母顺序
print(sorted(destination,reverse=True)) #相反的字母顺序
print(destination)
destination.reverse() #原来的顺序相反
print(destination)
destination.reverse() #再反
print(destination)
destination.sort() #按字母顺序
print(destination)
destination.sort(reverse=True) #反向的字母顺序
print(destination)